package main

import (
	"leftrana/moneybag/ex01/coinsortbench"
	"log"
	"os"
	"runtime/pprof"
	"time"
)

var amout int
var coinDenominations []int

func main() {
	f, err := os.Create("cpu.prof")
	if err != nil {
		panic(err)
	}
	pprof.StartCPUProfile(f)
	defer pprof.StopCPUProfile()
	log.Println("Cpu profile generating")
	BenchmarkMinCoinsAmount999()
	BenchmarkMinCoins2Amount999()
	BenchmarkMinCoins2Optimized()
	BenchmarkMinCoinsSl100()
	BenchmarkMinCoins2Sl100()
	BenchmarkMinCoins2OptimizedSl100()
	BenchmarkMinCoinsN()
	BenchmarkMinCoins2N()
	BenchmarkMinCoins2OptimizedN()
	BenchmarkMinCoins9999999()
	BenchmarkMinCoins29999999()
	BenchmarkMinCoins2Optimized9999999()
	BenchmarkMinCoinsRepetitions()
	BenchmarkMinCoins2Repetitions()
	BenchmarkMinCoins2OptimizedRepetitions()
	log.Println("Cpu profile saving")
	time.Sleep(10 * time.Second)
}

